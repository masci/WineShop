    private void typeComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_typeComboBoxActionPerformed
        // TODO add your handling code here:
         wineListComboBox.removeAllItems();
            if(typeComboBox.getSelectedItem().equals("Bianco")){
                for(int i=0; i < this.prodotti.size(); i++){
                       if(this.prodotti.elementAt(i).isBianco())
                           wineListComboBox.addItem(this.prodotti.elementAt(i).getNome());
                    }
            }
            else{
                if(typeComboBox.getSelectedItem().equals("Rosso")){
                    wineListComboBox.removeAllItems();
                    comboBoxQuantity.removeAllItems();
                    for(int i=0; i< this.prodotti.size(); i++){
                        if(this.prodotti.elementAt(i).isRosso()){
                            wineListComboBox.addItem(this.prodotti.elementAt(i).getNome());
                        }
                    }
                }
            }
    }//GEN-LAST:event_typeComboBoxActionPerformed

    private void wineListComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_wineListComboBoxActionPerformed
        // TODO add your handling code here:
        comboBoxQuantity.removeAllItems();
            for(int i=0; i < this.prodotti.size(); i++){
                if(this.prodotti.elementAt(i).getNome().equals(wineListComboBox.getSelectedItem()))
                    for(int j=0; j <= this.prodotti.elementAt(i).getPezziDisponibili(); j++){
                        comboBoxQuantity.addItem(j);
                        piecesAvailable.setText(""+this.prodotti.elementAt(i).getPezziDisponibili());
                        if(prodotti.elementAt(i).getNome().equals("Montefalco")){
                            labelMontefalco.setVisible(false);
                            imageLabelMnt.setVisible(true);
                        }
                        else{
                            if(prodotti.elementAt(i).getNome().equals("Grechetto")){
                                imageLabelMnt.setVisible(false);
                                imageLabel.setVisible(true);
                            }
                        }
                       
                    }
                }
    }//GEN-LAST:event_wineListComboBoxActionPerformed

